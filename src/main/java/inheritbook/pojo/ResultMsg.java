package inheritbook.pojo;

public class ResultMsg {
/*
 * 返回给前端的数据封装
 */
	private String msg;
	private String url;
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
